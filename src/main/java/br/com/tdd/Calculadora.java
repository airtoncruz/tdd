package br.com.tdd;

public class Calculadora {

    public int somar(int numA, int numB) {
        return numA + numB;
    }

    public int subtrair(int numA, int numB) {
        return numA - numB;
    }

    public int multiplicar(int numA, int numB) {
        return numA * numB;
    }

    public double dividir(double numA, double numB) {
        return numB / numA;
    }
}
